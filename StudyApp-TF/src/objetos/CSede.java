package objetos;

public class CSede {
	
	public int id;
	public String nombre;
	public int CUniversidad;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCUniversidad() {
		return CUniversidad;
	}
	public void setCUniversidad(int cUniversidad) {
		CUniversidad = cUniversidad;
	}
	

}
