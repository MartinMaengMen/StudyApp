package objetos;

public class CPregResp {

	public int id;
	public String comentario;
	public String foto_pregunta;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getComentario() {
		return comentario;
	}
	public void setComentario(String comentario) {
		this.comentario = comentario;
	}
	public String getFoto_pregunta() {
		return foto_pregunta;
	}
	public void setFoto_pregunta(String foto_pregunta) {
		this.foto_pregunta = foto_pregunta;
	}
	
	
}
